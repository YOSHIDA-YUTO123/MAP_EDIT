#include "player.h"

//===================================================
// コンストラクタ
//===================================================
CPlayer::CPlayer() : CCharacter3D(CCharacter3D::TYPE_PLAYER)
{

}

//===================================================
// デストラクタ
//===================================================
CPlayer::~CPlayer()
{
}

//===================================================
// 初期化処理
//===================================================
HRESULT CPlayer::Init(void)
{
	// 初期化処理
	if (FAILED(CCharacter3D::Init()))
	{
		return E_FAIL;
	}

	return S_OK;
}

//===================================================
// 終了処理
//===================================================
void CPlayer::Uninit(void)
{
	// 終了処理
	CCharacter3D::Uninit();
}

//===================================================
// 更新処理
//===================================================
void CPlayer::Update(void)
{
	// モーション更新処理
	CCharacter3D::UpdateMotion();

	// 更新処理
	CCharacter3D::Update();
}

//===================================================
// 描画処理
//===================================================
void CPlayer::Draw(void)
{
	// 描画処理
	CCharacter3D::Draw();
}

//===================================================
// クローンの作成
//===================================================
CCharacter3D* CPlayer::Clone(void) const
{
	// コピーコンストラクタでコピー
	auto clone = new CPlayer(*this);

	return clone;
}
